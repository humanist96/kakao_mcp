def match_template(report_name: str) -> DisclosureTemplate:
    """공시 제목으로 템플릿을 찾는다. 매칭 실패 시 기본 템플릿."""
    return next(
        (t for t in TEMPLATES if any(k in report_name for k in t.keywords)),
        DEFAULT_TEMPLATE,
    )
